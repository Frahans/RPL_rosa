public class Akun {

    private String kode_penggguna, email, username, password;

    public String getKode_penggguna() {
        return kode_penggguna;
    }

    public void setKode_penggguna(String kode_penggguna) {
        this.kode_penggguna = kode_penggguna;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

}
