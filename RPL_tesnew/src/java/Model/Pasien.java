package Model;

/**
 *
 * @author HP
 */
public class Pasien {

    private String kode_pasien, nama, alamat, ttl, jenis_kelamin, pekerjaan, 
            agama, tipe_pasien, tipe_jaminan, nomor_jaminan, nomor_telpon;

    public String getKode_pasien() {
        return kode_pasien;
    }

    public void setKode_pasien(String kode_pasien) {
        this.kode_pasien = kode_pasien;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getTtl() {
        return ttl;
    }

    public void setTtl(String ttl) {
        this.ttl = ttl;
    }

    public String getJenis_kelamin() {
        return jenis_kelamin;
    }

    public void setJenis_kelamin(String jenis_kelamin) {
        this.jenis_kelamin = jenis_kelamin;
    }

    public String getPekerjaan() {
        return pekerjaan;
    }

    public void setPekerjaan(String pekerjaan) {
        this.pekerjaan = pekerjaan;
    }

    public String getAgama() {
        return agama;
    }

    public void setAgama(String agama) {
        this.agama = agama;
    }

    public String getTipe_pasien() {
        return tipe_pasien;
    }

    public void setTipe_pasien(String tipe_pasien) {
        this.tipe_pasien = tipe_pasien;
    }

    public String getTipe_jaminan() {
        return tipe_jaminan;
    }

    public void setTipe_jaminan(String tipe_jaminan) {
        this.tipe_jaminan = tipe_jaminan;
    }

    public String getNomor_jaminan() {
        return nomor_jaminan;
    }

    public void setNomor_jaminan(String nomor_jaminan) {
        this.nomor_jaminan = nomor_jaminan;
    }

    public String getNomor_telpon() {
        return nomor_telpon;
    }

    public void setNomor_telpon(String nomor_telpon) {
        this.nomor_telpon = nomor_telpon;
    }

}
