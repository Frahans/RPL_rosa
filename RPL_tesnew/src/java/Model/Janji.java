public class Janji {
    private String kode_janji, kode_pasien, hariTanggal, jam;

    public String getKode_janji() {
        return kode_janji;
    }

    public void setKode_janji(String kode_janji) {
        this.kode_janji = kode_janji;
    }

    public String getKode_pasien() {
        return kode_pasien;
    }

    public void setKode_pasien(String kode_pasien) {
        this.kode_pasien = kode_pasien;
    }

    public String getHariTanggal() {
        return hariTanggal;
    }

    public void setHariTanggal(String hariTanggal) {
        this.hariTanggal = hariTanggal;
    }

    public String getJam() {
        return jam;
    }

    public void setJam(String jam) {
        this.jam = jam;
    }
    
}
